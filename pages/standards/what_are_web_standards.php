<h4 id="so_what_are"><span></span>So What Are Web Standards?</h4>

<p>Follwing "web standards" means following best practices for web technologies which the
<abbr title="World Wide Web Consortium">W3C</abbr> generates. Web standards focuses on three main web technologies;
<abbr title="HyperText Markup Language">HTML</abbr>, <abbr title="Extensible HyperText Markup Language">XHTML</abbr>
and <abbr title="Cascading Style Sheets">CSS</abbr>. With the combination of these technologies a developer should strive
to meet a certain level of accessibility which is described in the <abbr title="World Wide Web Consortium">W3C</abbr>'s
<abbr title="Web Content Accessibility Guidelines">WCAG</abbr>.</p>


