<h3><span></span>The Importance Of Web Standards</h3>

<h4 id="introduction"><span></span>Introduction</h4>

<p>This entire website discusses how to use web technologies correctly; so-called web standards. However, there needs a reasoning behind
learning this information. There are a few main arguments for the importance of web standards. This article discusses the main areas.</p>


<h4 id="separation"><span></span>Separation Of Content And Presentation</h4>

<p>Following web standards means the use of semantic markup which is often wrongly referred to as table-less markup.
The use of semantic markup is also reiterated by the importance of separation between content and presentation.
This is achieved by a combination of semantic markup - describing the content accurately and correctly - and
<abbr title="Cascading Style Sheets">CSS</abbr> which controls the complete presentation and layout.</p>


<h4 id="compatibility"><span></span>Cross Browser Compatibility</h4>

<p>Developing with web standards allows for good cross-browser compatibility and also means the site will support older browsers by degrading well.
Any combinations of screen sizes, image and script user preference can be ignored when using web standards as the technologies have
been created to allow for these differences and the important content will still be readable and usable.</p>


<h4 id="independence"><span></span>Device Independence</h4>

<p>A website designed with these best practices can be easily displayed on upcoming "future" technologies such as handheld devices like
<abbr title="Personal Digital Assistant">PDA</abbr>s and mobile phones. A more focused design can also be applied for when a site is printed.</p>


<h4 id="maintenance"><span></span>Maintenance</h4>

<p>The updating of an entire site design is extremely easy and quick as none of the content needs to be changed and all the presentation information
is defined in one area. Any changes made to the presentation file will affect a website globally. This also helps to keep consistency across
the site which vastly improves usability.</p>


<h4 id="bandwidth"><span></span>File Sizes, Bandwidth And Costs</h4>

<p>The use of semantic markup and the separation of presentation into a single document means that overall website file sizes are reduced. The effect of this
can easily be seen on small sites but shows up exponentially for for larger sites. This reduce in file sizes mean lower hosting costs, but more importantly
huge savings on bandwidth costs.</p>


<h4 id="accessibility"><span></span>Accessibility</h4>

<p>By meeting levels of accessibility a page is also usable more easily with assistive technologies such as screen readers. Meeting certain accessibility
targets is an increasing priority for many large online retailers as laws are now starting to be enforced.</p>


<h4 id="bottom_line"><span></span>The Bottom Line</h4>

<p>With the combination of all these practices coupled with a user-friendly design a page should be accessible to a huge majority of web users.
Also, using web standards saves you money. It's more economical!</p>

