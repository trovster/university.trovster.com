<h4><span></span>Introduction</h4>

<p id="intro">Andy Budd works as senior web designer for Message Digital Design Ltd. In his spare time he runs
<a href="http://www.skillswap.org/" title="Skillswap - a voluntary group set up by members of the local web design community ">SkillSwap.org</a>,
a project that provides free training for his local new media community. He is most widely know for his
<a href="http://www.andybudd.com/" title="Andy Budd's most excellent blog">blog</a>, where he writes about a wide range of
subjects including web design and web standards.</p>

