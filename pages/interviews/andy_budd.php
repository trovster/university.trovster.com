<dl id="interview">

	<dt id="q1">Q1. How did you get started in web design/development?</dt>
		<dd>
		<p>I started using the web as a means of researching destinations and keeping in touch with people, while backpacking around Asia.
		During these travels, I met a number of web designers and became interested in pursuing web design as a career. It was just at the start
		of the dot com boom, and even basic <abbr title="Hypertext Markup Language">HTML</abbr> skills were in high demand. I had this naive idea that I'd be
		able to work as a freelance web designer during the summer and then go travelling during the winter.</p>
		<p>
		On my return to England, I bought a cheap computer, and with the help of sites like webmonkey and ask doctor web,
		I taught myself basic web design skills. I set to work building up a bit of a portfolio of sites and was very
		soon working as a freelance web designer.</p>
	</dd>


	<dt id="q2">Q2. What is it about the Web design and development that interests you?</dt>
	<dd> 
		<p>I've been interested in computers and technology since my first Sinclair 48k. I liked the idea of pursuing a career in
		<abbr title="Information Technology">IT</abbr>, but realised I didn't have the patience or dedication to be a hard core programmer.
		I've always been a very inquisitive person and am never happier than when I'm learning new skills and then putting them into practice.
		Variety being the spice of life, it's the breadth of my work that keeps me interested. I also like the feeling of seeing
		something evolve before your eyes and the feeling of accomplishment when the jobs finished and the clients are happy.</p>
	</dd>


	<dt id="q3">Q3. What does the term "Web Standards" mean to you?</dt>
	<dd> 
		<p>Building a site to a set of known standards, rather than to a particular browser or set of proprietor tags.</p>
	</dd>


	<dt id="q4">Q4. What made you move towards developing using web standards?</dt>
	<dd> 
		<p>I guess reading sites like <a href="http://hotwired.lycos.com/webmonkey/" title="Webmonkey - now shut">Webmonkey</a> and
		<a href="http://www.alistapart.com/" title="A List Apart"><abbr title="A List Apart">ALA</abbr></a> got me interested in the potential of
		<abbr title="Cascading Style Sheets">CSS</abbr>, namely lower files sizes and search engine friendliness. At the same time I came across a local
		developer called Jeremy Keith via his site <a href="http://adactio.com/" title="Jeremy Keith's website">Adactio</a>. Jeremy was a keen standards
		advocate and his site was one of the few pure <abbr title="Cascading Style Sheets">CSS</abbr> sites around at the time. After a number of conversations
		with Jeremy about <abbr title="Cascading Style Sheets">CSS</abbr> and web standards, I decided to have a go at building my first pure
		<abbr title="Cascading Style Sheets">CSS</abbr> site.</p>
		<p>I found the process much more logical than the old table based, browser specific approach. Rather than coding to a browser,
		you would now code to a standard. If your design broke in a particular browser, you'd know it was the browser to blame, rather than your code.
		Obviously you'd still needed to take into account browser issues, but coding to a standard allowed you to remove one variable from the equation.</p>
	</dd>



	<dt id="q5">Q5. What were the hardest aspects of web standards development to get a grip on for you?</dt>
	<dd>
		<p>I guess the most difficult concepts for me were <abbr title="Cascading Style Sheets">CSS</abbr> positioning and floating. However,
		by far the most difficult aspect of web standards development is buggy, inconsistent browsers.</p>
	</dd>
	
	
	<dt id="q6">Q6. Do you think that developing sites using Web Standards is an achievable goal for web developers today?</dt>
	<dd> 
		<p>Absolutely. Remember that <abbr title="Hypertext Markup Language">HTML</abbr> 4.01 has been around since 1998 and despite what some people may have
		you believe, tables have not been deprecated. Most designers could start creating standards compliant sites tomorrow,
		just by adding the correct doctype to their pages.</p>
		<p>However, when most people talk about web standards, they are actually talking about tableless
		<abbr title="Cascading Style Sheets">CSS</abbr> based layouts and <abbr title="Extensible Hypertext Markup Language">XHTML</abbr>.
		Creating sites in this manner is complicated by a couple of issue. The most obvious problem is browser support. There are still quite
		a few buggy browsers kicking around and a large proportion of my development time is spent dealing with unruly user agents.</p>
		<p>The biggest problem however, is inertia. We all spent years learning how to build sites using tables, and most of us are now pretty good at it.
		Tableless design is a completely new skill with (in my experience) a 6 to 9 month learning curve.
		As such, it's much easier for most developers to continue building sites the way they have always done.
		The thing is, web standards are definitely the future, and there will come a time when these old techniques become redundant.</p>
	</dd>


	<dt id="q7">Q7. Can you tell us a bit about the <a href="http://www.webstandardsawards.com/" title="Web Standards Awards">Web Standards Awards</a>.
	How did it start, what are its aims and do you feel it is successful so far?</dt>
	<dd> 
		<p>I'd been meaning to put together a site highlighting well designed <abbr title="Cascading Style Sheets">CSS</abbr> sites for some time.
		I'd discussed the idea with a number of people but never got round to doing anything about it. Then a web designer named
		<a href="http://www.snowinteractive.com/" title="Johan Edlund's website">Johan Edlund</a> contacted me to ask my opinion of a site he was working on.
		That site was the Web Standards Awards. I thought the site was great, and kept bugging him every few weeks to see if it was finished.
		The site was about 90% complete. All it needed was an admin system and some content.</p>
		<p>At the same time, I noticed a post on
		<a href="http://css-discuss.incutio.com/" title="CSS-Discuss - the ultimae CSS discussion list"><abbr title="Cascading Style Sheets">CSS</abbr>-Discuss</a> from
		<a href="http://www.themaninblue.com/" title="Cameron Adam's website - far too talented for his own good">Cameron Adams</a> about the need for
		some kind of award/showcase site (this was all prior to the daily standard and the
		<a href="http://cssvault.com/" title="CSS Vault - an excellent showcase of CSS-based sites"><abbr title="Cascading Style Sheets">CSS</abbr> Vault</a>).
		I told Cameron about the <abbr title="Web Standards Awards">WSA</abbr> and, to cut a long story short, we both offered to help Johan finish the site off.
		I set up <abbr title="Movable Type">MT</abbr> and contacted potential judges, while Cameron wrote the copy and set up a submissions admin system.</p>
		<p>The purpose of the <abbr title="Web Standards Awards">WSA</abbr> is to highlight and reward well designed, standards based sites. It's to act as
		design inspiration to the many standards based designers out there and to prove that good design and standards are not mutually exclusive.</p>
	</dd>
	

	<dt id="q8">Q8. Do you think there is an element of web standards fascism in the web development community?</dt>
	<dd> 
		<p>Oh definitely, as you can probably tell by my replies to some of the comments on the <abbr title="Web Standards Awards">WSA</abbr> site
		and also my blog. It's extremely easy to cry foul if a site doesn't validate because of a couple of unencoded ampersands,
		or if the display isn't 100% perfect on an outdated or minority browser. However I think this attitude shows a lack of
		understanding about the pressures professional web designers are under, as well as a deep misunderstanding about web standards.</p>
		<p>Time is in huge demand in our industry, and despite peoples best intentions, bugs and anomalies are unavoidable.
		However it's the very fact that people are trying to produce standards compliant sites on such limited budgets and tight deadlines,
		that needs to be commended. After all, web standards are meant to help us, not to act as a cross to crucify people on. Unfortunately,
		the "all or nothing" approach of some standards Zealots just give people another reason not to adopt web standards.</p>
	</dd>
	

	<dt id="q9">Q9. <a href="http://www.skillswap.org/" title="Skillswap - a voluntary group set up by members of the local web design community">SkillSwap</a>
	- what it is all about and how it is going?</dt>
	<dd> 
		<p>The idea behind SkillSwap arose from my local web design mailing list. I realised that within the local community there were a huge range of skills available.
		Rather than pay for expensive training courses, I though people may be willing to swap some of their time and knowledge in return for the
		chance to learn things from other members of the community.</p>
		<p>So I started organising regular SkillSwap events. Basically, around once a month, somebody from the local new media community
		volunteers to teach a small group of peers about a subject of their choice. These sessions usually last for around three hours and
		are held in a local training centre. The response so far has been excellent. We've had a great selection of talks, all of which have
		been heavily over subscribed, and we generally don't have problems finding speakers.</p>
		<p>SkillSwap is currently a small, local project that I run by myself in my own time. However It's something that could easily work in
		other locations, something I'm interested in exploring in the future.</p>
	</dd>
	
	
