<dl id="interview">
	<dt id="q1">Q1. Russ: First of all, when did you first start developing with web standards?</dt>
	<dd>
		<p>Before mid-2003 the only thing that I used <abbr title="Cascading Style Sheets">CSS</abbr> for was to get some awesome hover effects on anchor tags.
		After discovering <abbr title="Jeffery Zeldman">JZ</abbr>'s "<a href="http://www.zeldman.com/" title="The Daily Report">The Daily Report</a>"
		I was really excited about this new way of coding web sites, but I'm not sure that I fully understood *why* it was a better way of doing things.
		Of course, the amount of time that has been put into Standards advocacy by people from all over the globe &mdash; through weblogs,
		articles and books &mdash; has now clarified that for me.</p>
		<p>Irrespective, at the time all the cool people were using Standards, so as a true lemming I stopped the development of my personal site in
		deformed tables and gave this <abbr title="Extensible Hypertext Markup Language">XHTML</abbr>/<abbr title="Cascading Style Sheets">CSS</abbr> thing a shot;
		pretty much a baptism of fire. Since then &mdash; November 27, 2003 &mdash; I don't think I've included a presentational table in any site I've done.</p>
	</dd>


	<dt id="q2">Q2. You became an overnight success with the launch of your site &mdash;
	<a href="http://www.themaninblue.com/writing/" title="The Man in Blue">Man in Blue</a>. Did you have any idea that it would be so well received?</dt>
	<dd>
		<p>At the time I developed it, I wasn't aware of many eye-catching Standards-based sites, apart from maybe
		<a href="http://www.stopdesign.com/" title="Doug Bowman's Stop Design">Stopdesign</a> (and anything else out of the Bowman House of Style), so I was trying
		to see what my years of <a href="http://www.k10k.net/">K10K</a> browsing could bring to the table. From that perspective I hoped it would make a splash,
		but the magnitude of that splash &mdash; and the longevity of its ripples &mdash; have truly surprised me.</p>
		<p>For the initial impression I can probably thank Doug Bowman who, after being sent several unmarked bills in small denominations, gave it a
		<a href="http://www.stopdesign.com/log/2003/12/01/maninblue.html" title="Bowman gives the man in blue a glowing report">glowing writeup on his weblog</a>.
		For its continued success... I suppose I can only thank the patience of the people who wade through my prose.</p>
	</dd>


	<dt id="q3">Q3. You are one of the rare individuals that straddles the fence between serious coding and design.
	Do you run the risk of losing a testicle in the process?</dt>
	<dd>
		<p>Though the future of the Adams clan may be at risk, I wouldn't have it any other way.</p>
		<p>The alluring essence of web development, as opposed to print design or application programming (the two established areas from which I
		feel web development can best draw comparisons), is that it allows me to exercise both my logical and creative tendencies. Not to say that
		print design doesn't include logicality or that programming isn't creative, but they both have distinctive processes that prevent immediate
		translation between the two. By combining these two previously disparate areas, the Internet has the ability to create something new &mdash;
		interactive beauty; elegant systems.</p>
		<p>Although many people preach a specialisation of skills on the Web &mdash; usability, information architecture, visual design, programming &mdash;
		I think that to truly grasp the potential of the medium you have to be able to straddle that divide, and that's what we're seeing the new breed of
		web developers doing. A great example of this is the usage of programming languages to create digital art, such as at
		<a href="http://processing.org/" title="processing.org - prgramming language used to create digital art">Processing</a>.</p>
	</dd>


	<dt id="q4">Q4. You recently said "<a href="http://www.themaninblue.com/writing/perspective/2004/07/02/" title="A design should  be based on usability, accessibility, aesthetics...">
	A design should  be based on usability, accessibility, aesthetics, but never on floats, lists or background images</a>". What did you mean by this?</dt>
	<dd>
		<p>What I meant was "Doug Bowman, <a href="http://www.stopdesign.com/present/2004/sydney/limits/?no=2" title="Bowman uses The Man in Blues idea, and Cameron intends to sue!">
		please use this for a presentation at Web Essentials 04</a>".</p>
		<p>No, Doug gave a great talk which neatly coincided with my own views: Web Standards are merely one way of doing something that has been around for a while,
		and will be around for much longer. Architects don't let a hammer dictate what they should build, so Web Standards
		shouldn't dictate the way that a web page is developed.</p>
		<p>We're already seeing Standards practices change &mdash; a lot of that brought about by experimentation. Without that experimentation there
		would be no innovation; without innovation, no improvement. If you continue to circumvent your design process by thinking in terms of what you *know*
		you can already implement, then you will never be confronted with problems you have to solve. Truly a boring life, and not a way in which to better
		yourself or your designs.</p>
	</dd>

	
	<dt id="q5">Q5. In January you created an online <a href="http://www.themaninblue.com/writing/perspective/2004/01/27/" title="CSS Scabble">
<abbr title="Extensible Hypertext Markup Language">XHTML</abbr>/<abbr title="Cascading Style Sheets">CSS</abbr> based Scrabble</a>.
	Was it hard to build?</dt>
	<dd>
		<p>The <abbr title="Hypertext Markup Language">HTML</abbr> code and visual design were fairly trivial to come up with. (Scrabble isn't exactly the best looking board game to hit the shelves)
		It was the behaviour layer &mdash; the JavaScript &mdash; that was the hardest part.</p>
		<p>From my initial plans to <abbr title="Cascading Style Sheets">CSS</abbr> style one individual Scrabble tile, it turned into a mini software engineering project.
		When you start something at 10PM that you thought would take an hour, and you end up modularising code at 5AM, it's kind
		of hard to optimise your functions. Needless to say, the code isn't the neatest, but <a href="http://www.survex.com/~olly/" title="Olly Betts' site">Olly Betts</a>
		managed to extend it and add some functionality, so it can't be that illegible.</p>
	</dd>


	<dt id="q6">Q6. In February, along with <a href="http://www.snowinteractive.com/" title="Johan Edlund's Snow Interactive site">Johan Edlund</a>
	and <a href="http://www.andybudd.com/" title="Andy Budd's site">Andy Budd</a>, you launched the Web Standards Awards. How are the Awards going?</dt>
	<dd> 
		<p>The awards are motoring along quite nicely. There's been quite a flood of <abbr title="Extensible Hypertext Markup Language">XHTML</abbr>/<abbr title="Cascading Style Sheets">CSS</abbr> galleries around the Web, but I get the
		feeling that we're the only one that really looks at the big picture &mdash; design, validation, usability, semantic code &mdash;
		whereas most of the others seem to be happy to put up a good looking site if it simply uses no tables. You might think that finding
		well designed sites that have quality code amidst all this Standards fervour would be easy, but more often than not good looking
		sites don't match that quality under the hood. That's why you'll find less sites up at the Web Standards Awards than elsewhere...
		and why it makes it such an honour to win one.</p>
		<p>Of course, as Mike Davidson <a href="http://www.webstandards.org/buzz/archive/2004_10.html#a000460" title="A controversial statement by Mike Davidson">
		controversially stated</a>, how far do you want to take Standards? We're fairly strict on code quality and validation, but we like to take a holistic approach.
		If there are other mitigating factors which have influenced validation, etc. then we'll take those into account when making a decision to give out an award.</p>
	</dd>

	
	<dt id="q7">Q7. In March you introduced "<a href="http://www.themaninblue.com/writing/perspective/2004/02/19/" title="Accessible, stylish form layout">
	Accessible, stylish form layout</a>". Did you learn much about browser support during the process?</dt>
	<dd>
		<p>I would have to say that forms are probably the least standardised objects across browsers. All other elements are pretty much an
		invisible square box with standard decoration applied to it, but if you're using semantic and accessible form structures &mdash; fieldsets,
		legends, labels and inputs &mdash; then you'll probably have no chance of getting a form to display consistently across browsers, let alone operating systems.</p>
		<p>Aside from differences between the way padding and margin are handled inside form elements by browsers, I think perhaps their unique relationship
		with the OS is what makes them such a difficult beast to handle on a web page. You're using widgets that Micrsofot or Linux or Apple have pre-determined,
		and sometimes it's just not possible to mesh their ideas with the ideas on your web page. This can be a good thing &mdash; forms are about extracting
		data from a user who has better things to do, so the more standardised and easier this process is for them then the better it is all around. But if
		you're a pixel-retentive designer then it's probably not your idea of perfection.</p>
	</dd>


	<dt id="q8">Q8. You recently launched <a href="http://ee.hearne.com.au/" title="Energy Express - with an amazingly complex nav system">Energy Express</a>.
	The navigation is visually amazing, yet it uses a basic list for structure. How was it achieved?</dt>
	<dd>
		<p>Ummmm... just let me remind myself about the code... oh yes. </p>
		<p>It's all bare bones <abbr title="Hypertext Markup Language">HTML</abbr>, except for some special classes/IDs. IDs are needed for the first and last items in the main menu because
		they are visually different from the "generic" items and require different background images. If there was cross-browser support for the <code>:first-child</code>
		<abbr title="Cascading Style Sheets">CSS</abbr> selector and a couple of others, then these would probably be redundant.</p>
		<p>The classes allow the <abbr title="Cascading Style Sheets">CSS</abbr> to know when a menu item is on i.e. highlighted, and when it is extended i.e. has visible child items.
		If I was coding this now I would probably remove the inline classes from the list and use body classes to modify styles using the stylesheet cascade.</p>
		<p>For the background images themselves it's all about careful tiling ... and drop shadows of course</p>
	</dd>


	<dt id="q9">Q9. In June you launched your <a href="http://www.themaninblue.com/news/" title="The new portfolio site is launched">new portfolio site</a>.
	What sort of obstacles did you have to overcome during development?</dt>
	<dd>
		<p>The biggest obstacle was designing something which I liked (you're your own worst client) and which was cutting-edge enough for me to warrant putting time into.</p>
		<p>The second biggest obstacle was cross-browser floats. There's four main floats on the site, each with about four other floats contained within them,
		and they all had to be pixel perfect. The only solution to this is hacks. Hacks for IE 5.x, hacks for IE 5.0, hacks for IE 5.5, hacks for IE 6.0,
		hacks for Firefox, hacks for Opera. I think I spent about four days on Position Is Everything trying to figure out ways not to use them, but even
		Big John came up empty, so there was nothing to do but h***.</p>
		<p>Third obstacle? Image slicing. If I ever tell you that I'm going to design another site with 8 alternate stylesheets (originally 16), please slap me.</p>
		<p>Still, I think it was worth it.</p>
	</dd>


