<h4><span></span>Introduction</h4>

<p id="intro">Cameron Adams, aka the <a href="http://www.themaninblue.com" title="Cameron Adams' Website">Man in Blue</a>, has been designing things
on and off the Web since 1997. Based in Melbourne, Australia, a university background in Computer Science and over seven years experience in
graphic design have made the Internet his home away from home &mdash; where he revels in making usable, accessible and likeable interfaces.</p>

