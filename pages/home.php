<h3><span></span>All Levels</h3>

<p>This website is aimed at all levels of web developers, from people interested in starting a personal website to established
"web masters". There are basic articles discussing the use of semantic markup and the separation of presentation and content using
<abbr title="Cascading Style Sheet">CSS</abbr>, along with tutorials and walkthroughs of basic examples. Finally there are articles
discussing advanced <abbr title="Cascading Style Sheet">CSS</abbr>-based techniques which are commonly required in design.</p>

<h3><span></span>Accessibility</h3>

<p>This website uses the best practices in web accessibility to reach the greatest possible audience. This includes the use of 
<abbr title="Cascading Style Sheet">CSS</abbr> for presentation &amp; accesskeys. More information about all the practices can be 
found in the <a href="accessibility_statement/">accessibility statement</a>.</p>

