<h3><span></span>Unrestricted Access</h3>

<p>Is a browser-independent, cross-platform &amp; multi-medium application website focusing on web standards and accessibility.</p>

<h3><span></span>Multiple Designs</h3>

<p>You can browse this entire website using a design layout you feel comfortable with. There are 21 pre-existing designs already
implemented, but you can apply your own style easily.</p>

<p><a href="colophon/#style_changer">View the existing styles</a> and find out <a href="development_articles/create-your-own-stylesheet">how to use your own</a>.</p>

<h3><span></span>Independence</h3>

<p>The practices discussed on this website are also implemented throughout. This means the site achieves platform and user agent (browser) independence.
The website works across Microsoft Windows, Linux and <acronym title="Macintosh">Mac</acronym>s. As well as on handheld devices such as
<abbr title="Personal Digital Assistant">PDA</abbr>s and mobiles and if printed out.</p>

