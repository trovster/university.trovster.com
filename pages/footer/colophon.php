<h4 id="abbreviations"><span></span>Abbreviations and Acronyms</h4>

<p>Abbreviations and acronyms are both used extensively throughout the site. 
<a href="glossary/" title="Glossary of abbreviations used throughout the site">A glossary of the abbreviations, acronyms and initialisms used</a>.
They are marked using the following definition.</p>

<blockquote cite="http://whatis.techtarget.com/definition/0,,sid9_gci211518,00.html">
<p>An acronym (pronounced AK-ruh-nihm, from Greek acro- in the sense of extreme or tip and onyma or name) is
an abbreviation of several words in such a way that the abbreviation itself forms a word. According to Webster's,
the word doesn't have to already exist; it can be a new word. Webster's cites &quot;snafu&quot; and &quot;radar&quot;,
two terms of World War Two vintage, as examples. Implicit is the idea that the new word has to be pronounceable and
ideally easy to remember.</p>

