<h3><span></span>Accessibility</h3>


<h4 id="accesskeys"><span></span>Accesskeys</h4>

<p>Most browsers support accesskeys which allow keyboard shortcuts to important areas defined on the web site.</p>

<p>These can be accessed on Windows by pressing <abbr title="Alternative Text">ALT</abbr> + accesskey, and on Macintosh using Control + accesskey</p>


<h4 id="browser_compatability"><span></span>Browser Compatability</h4>

<p>This site has been designed to work with all browsers on all platforms. Design considerations have been taken to make sure
the sites works on all modern browsers, as well as depreciating effectively in older browsers. The site has been design to work well with 
text-only browsers and with assistive technologies, such as screen readers.</p>

<p>The site will also work on small screen devices such as <abbr title="Personal Digital Assistant">PDA</abbr>s and the latest mobile phones.</p>


<h4 id="standards_compliance"><span></span>Standards Compliance</h4>

<p>All of the docments on this site validate as <abbr title="Extensible HyperText Markup Language">XHTML</abbr> 1.0 Strict.
The also site uses structured, semantic mark-up.</p>

<p>The site is constructed using Cascading Style Sheets (<abbr title="Cascading Style Sheet">CSS</abbr>) to present the different styles.
If the browser or device you are using does not support <abbr title="Cascading Style Sheet">CSS</abbr>, the site will still
be presented as a structured, well-formed document.</p>

<p>All of the pages on this site meet the highest accessibility standards. The pages comply to priorities 1, 2 and 3
of the World Wide Web Consoritums (<abbr title="World Wide Web Consoritum">W3C</abbr>) Web Content Accessibility Guidelines
(<abbr title="Web Content Accessibility Guidelines">WCAG</abbr>), which are part of the consortiums Web Accessibility Initiative
(<abbr title="Web Accessibility Initiative">WAI</abbr>). This means they are <abbr title="Web Content Accessibility Guidelines">WCAG</abbr>
"AAA" approved. The site has been tested using both Cynthia Says and Bobby which are automated accessibility tools which
apply the <abbr title="Web Content Accessibility Guidelines">WCAG</abbr>.</p>


<h4 id="images"><span></span>Images</h4>

<p>All decorative images are nested within the stylesheets. All functional and content-related images used on this
site include descriptive <abbr title="Alternative Text">ALT</abbr> attributes.
Complex images (such as figures) include a D-link to explain the significance of each image to readers who cannot,
or choose not to, view images. Style sheets are used to hide the D-links in visual browsers.</p>


<h4 id="links"><span></span>Links</h4>

<p>All links are written so that they sense out of context of the original article. Most links have title attributes which
describe information concerned with the link in more detail. No links contain javascript and therefore work if this scripting
is turned off. No links open new windows.</p>

