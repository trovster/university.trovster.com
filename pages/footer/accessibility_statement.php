<h4 id="printing"><span></span>Printing</h4>

<p>A separate style sheet is used for printing this site. There is a print-preview option at the bottom of the articles and interviews.
The following differences are used:</p>

<ul>
<li>A serif font is used.</li>
<li>The header is condensed &amp; visual images are removed</li>
<li>Black &amp; White colour scheme</li>
<li>Links, abbreviations and acronyms are all expanded.</li>
<li>Navigational links are removed.</li>
<li>Table headers are repeated on each page.</li>
</ul>

