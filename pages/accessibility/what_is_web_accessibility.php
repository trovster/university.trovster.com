<h3><span></span>What Is Web Accessibility?</h3>

<h4 id="introduction"><span></span>Introduction</h4>

<p>Accessibility has two main specific standards on the internet. There is the <acronym title="World Wide Web Consortium">W3C</acronym>'s
Web Content Accessibility Guidelines (WCAG) and the United States of America's Section 508 of the Workforce Rehabilitation Guidelines.
In the United Kingdom, accessibility on the web is covered under Code of Practice in Disability Discrimination Act.</p>


<h4 id="w3c_wcag"><span></span>Web Content Accessibility Guidelines</h4>

<p>The <acronym title="Web Content Accessibility Guidelines">WCAG</acronym> was written by an international panel of accessibility experts
and is comprised of three priority levels. These are defined as:</p>

<blockquote cite="http://www.w3.org/TR/WAI-WEBCONTENT/#priorities">
<ul>
<li>Priority Level 1 (A);<br />
<p>A Web content developer <strong>must</strong> satisfy this checkpoint. Otherwise, one or more groups will find it impossible to access
information in the document. Satisfying this checkpoint is a basic requirement for some groups to be able to use Web documents.</p></li>

<li>Priority Level 2 (AA);<br />
<p>A Web content developer <strong>should</strong> satisfy this checkpoint. Otherwise, one or more groups will find it difficult
to access information in the document. Satisfying this checkpoint will remove significant barriers to accessing Web documents.</p></li>

<li>Priority Level 3 (AAA);<br />
<p>A Web content developer <strong>may</strong> address this checkpoint. Otherwise, one or more groups will find it somewhat
difficult to access information in the document. Satisfying this checkpoint will improve access to Web documents.</p></li>
</ul>
<p><cite><a href="http://www.w3.org/TR/WAI-WEBCONTENT/#priorities" title="Web Content Accessibility Guidelines 1.0">http://www.w3.org/TR/WAI-WEBCONTENT/#priorities</a></cite></p>
</blockquote>


<h4 id="uk_legislation"><span></span><abbr title="United Kingdom">UK</abbr> Legislation</h4>

<p>The <a href="http://www.drc.org.uk/open4all/law/Code%20of%20Practice.pdf" title="Code Of Practice Guidelines (PDF)">Code of Practice</a> from the
Disability Rights Commission (DRC) contains specifics about accessibility in websites. Specifically the following sections:</p>

<blockquote cite="http://www.alistapart.com/articles/accessuk/">
<ul>
<li><p><strong>2.2 (p7)</strong>: The Act makes it unlawful for a service provider to discriminate against a disabled person by refusing to provide any service
which it provides to members of the public.</p></li>

<li><p><strong>4.7 (p39)</strong>: From 1st October 1999 a service provider has to take reasonable steps to change a practice which makes it
unreasonably difficult for disabled people to make use of its services.</p></li>

<li><p><strong>2.13 - 2.17 (p11-13)</strong>: What services are affected by the Act? An airline company provides a flight reservation and booking service to
the public on its website. This is a provision of a service and is subject to the act.</p></li>

<li><p><strong>5.23 (p71)</strong>: For people with visual impairments, the range of auxiliary aids or services which it might be reasonable to provide to
ensure that services are accessible might include &hellip; accessible websites.</p></li>
</ul>

<p><cite><a href="http://www.alistapart.com/articles/accessuk/" title="Web Accessibility and UK Law: Telling It Like It Is">http://www.alistapart.com/articles/accessuk/</a></cite></p>
</blockquote>


<h4 id="why_required"><span></span>Why Is This Required?</h4>

<blockquote cite="http://www.w3.org/WAI/">
<p>The power of the Web is in its universality. Access by everyone regardless of disability is an essential aspect.</p>
<p><cite>Tim Berners-Lee - <a href="http://www.w3.org/WAI/" title="Web Accessibility Initiative Homepage">http://www.w3.org/WAI/</a></cite></p>
</blockquote>

<p>The above quote by Tim Berners-Lee, the inventor of the <abbr title="World Wide Web">WWW</abbr>, sums up the why accessibility is important
for websites. The web, in it's infancy, was designed to give greater access to information. The web has grown since this but accessing information
is still it's primary application.</p>

<p>The web is now a much more user-friendly and graphical media but this should distract from it's main purpose &mdash; providing information. Information is
useless if it's not accessible by those who seek it. Therefore maintaining a certain level of accessibility is important for anyone who uses
the <abbr title="World Wide Web">WWW</abbr>. Making a website accessible doesn't mean that it should conform to the lowest common denominator, and be bland with no
graphical interaction. Graphical websites can be extremely accessible and conform to the highest level of the
<abbr title="Web Content Accessibility Guidelines">WCAG</abbr>.</p>

<p>It may seem that making an accessible website takes a lot of time and that there are a lot of different rules to follow. People may also argue that making a
website accessible restricts possibilities of certain layouts and a heavy use of images.</p>

<p>If you follow certain simple rules, write semantic markup and use <abbr title="Cascading Style Sheets">CSS</abbr> to present your design then it'll
become clear that a complex yet accessible website is fairly straight foward to achieve.</p>

<p>Keeping accessibility in mind when building a website doesn't just benefit disabled people. Research has proven that an accessible page is
navigated by all testers quickly.</p>

